package Tuan1;

public class Bai6 {
	public static void main(String[] args) {
		System.out.println("Binh phuong cua cac so tu 1 den 10: ");
		for(int i=1; i<=10; i++) {
			System.out.println(i + "^2 = " + (i * i));
		}
		
		System.out.println("Lap phuong cua cac so tu 1 den 10 la: ");
		for(int j=1; j<=10; j++) {
			System.out.println(j + "^3 = " + (j*j*j));
		}
	}
}
